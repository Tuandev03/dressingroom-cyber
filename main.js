// Đối tượng DOM
const navPillsContainer = document.querySelector(".nav-pills");
const tabContentContainer = document.querySelector(".tab-content");
const modelContainer = document.querySelector(".model");

// Render menu tab
data.navPills.forEach((tab) => {
  const tabItem = document.createElement("li");
  tabItem.classList.add("nav-item");
  const tabLink = document.createElement("a");
  tabLink.classList.add("nav-link");
  tabLink.textContent = tab.showName;
  tabLink.dataset.type = tab.type;
  tabLink.href = "#" + tab.tabName;
  tabLink.addEventListener("click", () => renderTabContent(tab.type));
  tabItem.appendChild(tabLink);
  navPillsContainer.appendChild(tabItem);
});

// Render tab content
function renderTabContent(type) {
  const tabPanes = data.tabPanes.filter((pane) => pane.type === type);
  tabContentContainer.innerHTML = "";
  tabPanes.forEach((pane) => {
    const tabPane = document.createElement("div");
    tabPane.classList.add("tab-pane", "fade");
    tabPane.id = pane.id;
    const img = document.createElement("img");
    img.src = pane.imgSrc_jpg;
    img.alt = pane.name;
    img.addEventListener("click", () => dressUp(pane));
    // img.classList.add("img-tabPanes");
    tabPane.appendChild(img);
    tabContentContainer.appendChild(tabPane);
  });
}

